package DAO;

/**
 *
 * @author DELL
 */
public class LabGroup {

    private int id;
    private String name;
    private int no ; 
    public LabGroup(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public LabGroup() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getNo() {
        return no;
    }

    public void setNo(int no) {
        this.no = no;
    }

}
